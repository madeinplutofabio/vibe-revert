// SECTION 4: empty-findings defensive case (CLI refuses; renderer renders)
// =============================================================================

describe("renderFixPrompt — empty-findings defensive case", () => {
  it("renders `## Findings (0)` with no per-finding blocks and never throws", () => {
    // Per D86 the CLI refuses BEFORE calling the renderer when
    // results is empty (and additionally removes any stale sibling
    // fix-prompt.txt). The renderer stays defensively correct so
    // the business-rule-vs-rendering boundary is clean — a future
    // caller that bypasses the CLI refusal gets a structurally
    // valid prompt with zero findings rather than a thrown error.
    const file = makeReportFile({ results: [] });
    const out = renderFixPrompt(makeRenderInput({ file }));
    expect(out.includes("## Findings (0)")).toBe(true);
    // No per-finding header anywhere in the output.
    expect(out.includes("### [")).toBe(false);
  });
});

