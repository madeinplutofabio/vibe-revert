// Unit tests for packages/reporters/src/fix-prompt-render.ts
// (M E Step 2 file 5 — fix-prompt renderer assembly + barrel surface).
//
// Seven sections covering distinct responsibilities:
//
//   1. ASSEMBLY & STRUCTURAL INVARIANTS — renderFixPrompt assembles
//      the seven D85 sections in the locked order, separated by
//      exactly one blank line, with exactly one trailing LF and
//      zero `\r` characters. Determinism is locked: same input →
//      byte-identical output across repeated calls. These are the
//      contract D81 + D85 invariants this file polices.
//
//   2. TASK SECTION CONDITIONAL (D85.3) — when `file.report.task`
//      is present + non-empty after inline normalization, the
//      `Task: <value>` line appears between source attribution and
//      repo context; otherwise it is omitted entirely (no
//      placeholder section, no spurious blank-line-only gap).
//
//   3. SOURCE-KIND BRANCHING (D85.6) — session-bound emits the
//      `viberevert rollback <session_id>` next-steps variant;
//      ad-hoc emits the git/checkpoint variant (rollback is
//      session-scoped and would not apply).
//
//   4. EMPTY-FINDINGS DEFENSIVE CASE — renderer renders a valid
//      `## Findings (0)` section with no per-finding blocks; never
//      throws. The CLI enforces the empty-findings refusal BEFORE
//      reaching the renderer per D86, but the renderer stays
//      defensively correct so the business-rule-vs-rendering
//      boundary is clean.
//
//   5. PRODUCT-VERSION + FOOTER (D85.9) — productVersion threads
//      through to the footer's `Generated by VibeRevert v<version>`
//      line; the report_id appears in BOTH the source-attribution
//      line and the footer; file.written_at appears in the footer's
//      `Report written at <written_at>` line.
//
//   6. BARREL SMOKE — renderFixPrompt is reachable through
//      ../src/index.js (the package's public barrel); M C report
//      and M D receipt exports remain callable (regression gate
//      against accidental displacement during the M E barrel
//      update).
//
//   7. HELPER-LEAKAGE LOCK — every export from
//      fix-prompt-template.ts (normalizeInline / normalizeBlock /
//      sortFindings / truncateFindings / renderPreamble / ... /
//      RECOMMENDATION_FALLBACK_LOW / MAX_FINDINGS / ...) MUST NOT
//      appear on the barrel. The barrel's only fix-prompt surface
//      is `renderFixPrompt` + `FixPromptRenderInput`. Catches
//      accidental promotion of file-local helpers to the public
//      surface; generated one-per-name so each failure names the
//      leaked helper directly.
//
// Template-branch smoke coverage: session-bound vs ad-hoc, task
// present vs absent, footer/version threading, empty findings, and
// section order. Detailed risk-level sorting, recommendation
// fallback, summary rendering, normalization, truncation, and
// evidence rendering live in fix-prompt-template.test.ts.
//
// Fixture-validity discipline: every ReportFile fixture flows through
// ReportFileSchema.parse via makeReportFile, mirroring the receipt
// fixture-validity pattern from receipt-render.test.ts. Risk level is
// INFERRED from the contained findings (max by rank) rather than
// hard-defaulted to "low" — keeps fixtures semantically honest if
// SessionReportSchema later adds a risk_level/results consistency
// refine. `rollback_available` mirrors `kind === "session_bound"` for
// the same semantic-honesty reason (ad-hoc reports have no owning
// session and cannot be rolled back per M D).

