describe("@viberevert/reporters barrel — fix-prompt helper-leakage lock", () => {
  // Every export from fix-prompt-template.ts is INTERNAL. The
  // barrel's only fix-prompt surface is `renderFixPrompt` +
  // `FixPromptRenderInput`. If a future maintainer accidentally
  // re-exports a template helper through the barrel — turning an
  // internal contract knob into part of the public API — this
  // lock fires. One assertion per name so each failure names the
  // leaked helper directly (mirrors receipt-render.test.ts's
  // per-name generated lock).

  const FORBIDDEN_FIX_PROMPT_HELPER_NAMES = [
    // Constants
    "PREAMBLE_PARAGRAPHS",
    "MAX_FINDINGS",
    "RECOMMENDATION_FALLBACK_HIGH",
    "RECOMMENDATION_FALLBACK_LOW",
    // Normalization helpers
    "normalizeInline",
    "normalizeBlock",
    // Enum gate
    "validateAndUppercaseLevel",
    // Sort + truncation
    "sortFindings",
    "truncateFindings",
    // Per-section render helpers
    "renderPreamble",
    "renderSourceAttribution",
    "renderTaskSection",
    "renderRepoContext",
    "renderEvidenceEntry",
    "renderRecommendation",
    "renderFinding",
    "renderFindingsSection",
    "renderNextSteps",
    "renderFooter",
  ];

  for (const helperName of FORBIDDEN_FIX_PROMPT_HELPER_NAMES) {
    it(`${helperName} is NOT exported from the barrel`, () => {
      expect(ReportersBarrel).not.toHaveProperty(helperName);
    });
  }
});
