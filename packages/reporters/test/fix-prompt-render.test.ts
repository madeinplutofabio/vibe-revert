describe("renderFixPrompt — productVersion threading and footer (D85.9)", () => {
  it("productVersion appears in the footer's `Generated by VibeRevert v<version>` line", () => {
    const file = makeReportFile({
      results: [makeResult({ level: "high", id: "ck.a", title: "A" })],
    });
    const out = renderFixPrompt(makeRenderInput({ file, productVersion: "9.9.9-test" }));
    expect(out.includes("Generated by VibeRevert v9.9.9-test from report")).toBe(true);
  });

  it("report_id appears in BOTH the source-attribution line and the footer (ad_hoc rpt_<ULID>)", () => {
    const file = makeReportFile({
      kind: "ad_hoc",
      results: [makeResult({ level: "high", id: "ck.a", title: "A" })],
    });
    const out = renderFixPrompt(makeRenderInput({ file }));
    const idxSourceLine = out.indexOf(`Source report: ${REPORT_ID}`);
    const idxFooterLine = out.indexOf(`from report ${REPORT_ID}.`);
    expect(idxSourceLine).toBeGreaterThan(-1);
    expect(idxFooterLine).toBeGreaterThan(idxSourceLine);
  });

  it("file.written_at appears in the footer's `Report written at <written_at>` line", () => {
    // The footer timestamp is the SOURCE REPORT's written_at, NOT
    // the prompt-generation time. The renderer never samples its
    // own clock (D29 / D90.5).
    const file = makeReportFile({
      results: [makeResult({ level: "high", id: "ck.a", title: "A" })],
    });
    const out = renderFixPrompt(makeRenderInput({ file }));
    expect(out.includes(`Report written at ${FIXED_TIME}.`)).toBe(true);
  });
});

