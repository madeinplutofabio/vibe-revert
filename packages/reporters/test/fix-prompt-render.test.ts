const VALID_ULID = "01ABCDEFGHJKMNPQRSTVWXYZ23";
const SESSION_ID = `sess_${VALID_ULID}`;
const REPORT_ID = `rpt_${VALID_ULID}`;
const FIXED_SHA = "abcdef0123456789abcdef0123456789abcdef01";
const FIXED_TIME = "2026-01-01T00:00:00Z";

/**
 * Compute the maximum risk level present in a set of findings (low <
 * medium < high < critical). Used by `makeReportFile` to keep the
 * fixture's report-level `risk_level` semantically consistent with
 * the contained findings — important defense if SessionReportSchema
 * later adds a "risk_level must be >= max(results.level)" refine.
 *
 * Empty results array → "low" (no findings means the report's overall
 * risk is the floor value; matches the convention used elsewhere in
 * the codebase for empty-findings reports).
 */
function inferRiskLevel(results: readonly CheckResult[]): RiskLevel {
  const rank: Record<RiskLevel, number> = {
    low: 0,
    medium: 1,
    high: 2,
    critical: 3,
  };

  let max: RiskLevel = "low";
  for (const result of results) {
    if (rank[result.level] > rank[max]) {
      max = result.level;
    }
  }
  return max;
}

function makeResult(opts: {
  level: RiskLevel;
  id?: string;
  category?: string;
  title?: string;
  message?: string;
  recommendation?: string;
}): CheckResult {
  const requiresReco = opts.level === "high" || opts.level === "critical";
  const reco = opts.recommendation ?? (requiresReco ? "Test recommendation." : undefined);
  const base = {
    id: opts.id ?? "test.id",
    category: opts.category ?? "test",
    level: opts.level,
    confidence: "medium" as const,
    title: opts.title ?? "Test title",
    message: opts.message ?? "Test message",
    evidence: [{ detail: "test detail" }],
  };
  return reco === undefined ? base : { ...base, recommendation: reco };
}

function makeReportFile(
  opts: {
    kind?: "session_bound" | "ad_hoc";
    results?: readonly CheckResult[];
    detectedFrameworks?: readonly string[];
    riskLevel?: RiskLevel;
    summary?: string;
    task?: string;
    sinceKind?: ReportFile["since_kind"];
    sinceRef?: string;
  } = {},
): ReportFile {
  const kind = opts.kind ?? "session_bound";
  const reportId = kind === "session_bound" ? SESSION_ID : REPORT_ID;
  // Default since_kind matches the locked refine: session_bound ↔
  // {session_id, active_session}; ad_hoc ↔ {checkpoint_id,
  // checkpoint_name, git_ref}. Defaults pick one from each set.
  const sinceKind: ReportFile["since_kind"] =
    opts.sinceKind ?? (kind === "session_bound" ? "session_id" : "checkpoint_name");
  const sinceRef = opts.sinceRef ?? (kind === "session_bound" ? SESSION_ID : "baseline");

  // Compute results first so risk_level can be INFERRED from them.
  // Caller can still override via `opts.riskLevel` (e.g., to test a
  // report whose declared risk exceeds its contained findings).
  const results = opts.results ? [...opts.results] : [];
  const riskLevel = opts.riskLevel ?? inferRiskLevel(results);

  const report: SessionReport = {
    schema_version: "1.0",
    session_id: reportId,
    started_at: FIXED_TIME,
    detected_frameworks: opts.detectedFrameworks ? [...opts.detectedFrameworks] : [],
    risk_level: riskLevel,
    results,
    changed_files: [],
    // Ad-hoc reports have no owning session and cannot be rolled
    // back per M D — `rollback_available` mirrors `kind` so the
    // fixture stays semantically honest.
    rollback_available: kind === "session_bound",
    ...(opts.task !== undefined ? { task: opts.task } : {}),
    ...(opts.summary !== undefined ? { summary: opts.summary } : {}),
  };
  const file = {
    schema_version: REPORT_FILE_SCHEMA_VERSION,
    kind,
    report_id: reportId,
    since_kind: sinceKind,
    since_ref: sinceRef,
    since_resolved_sha: FIXED_SHA,
    written_at: FIXED_TIME,
    report,
  };
  return ReportFileSchema.parse(file);
}

function makeRenderInput(opts: {
  file: ReportFile;
  productVersion?: string;
}): FixPromptRenderInput {
  return {
    file: opts.file,
    productVersion: opts.productVersion ?? "0.7.0-beta",
  };
}

