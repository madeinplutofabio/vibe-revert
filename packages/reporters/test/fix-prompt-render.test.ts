describe("renderFixPrompt — next-steps variant (D85.6)", () => {
  it("session_bound emits the `viberevert rollback <session_id>` clause", () => {
    const file = makeReportFile({
      kind: "session_bound",
      results: [makeResult({ level: "high", id: "ck.a", title: "A" })],
    });
    const out = renderFixPrompt(makeRenderInput({ file }));
    expect(out.includes(`recover with \`viberevert rollback ${SESSION_ID}\``)).toBe(true);
  });

  it("ad_hoc emits the `recover with git or a prior checkpoint; rollback is session-scoped.` clause and NO rollback command", () => {
    const file = makeReportFile({
      kind: "ad_hoc",
      results: [makeResult({ level: "high", id: "ck.a", title: "A" })],
    });
    const out = renderFixPrompt(makeRenderInput({ file }));
    expect(
      out.includes("recover with git or a prior checkpoint; rollback is session-scoped."),
    ).toBe(true);
    // Defensive: the session-bound rollback clause MUST NOT appear
    // anywhere in an ad_hoc prompt (it would mislead the agent —
    // rollback is session-scoped per D80 / M D).
    expect(out.includes("viberevert rollback ")).toBe(false);
  });
});

