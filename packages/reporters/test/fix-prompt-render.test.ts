// SECTION 1: assembly & structural invariants (D81 + D85)
// =============================================================================

describe("renderFixPrompt — assembly & structural invariants", () => {
  it("output starts with the locked three-paragraph preamble verbatim", () => {
    const file = makeReportFile({
      results: [makeResult({ level: "high", id: "ck.a", title: "A" })],
    });
    const out = renderFixPrompt(makeRenderInput({ file }));
    const preamble = PREAMBLE_PARAGRAPHS.join("\n\n");
    expect(out.startsWith(`${preamble}\n\n`)).toBe(true);
  });

  it("sections appear in the locked D85 order (preamble → source → repo → findings → next-steps → footer)", () => {
    // No `task` supplied, so the optional task section is omitted —
    // this assertion verifies the base six-section order with the
    // optional task section absent.
    const file = makeReportFile({
      results: [makeResult({ level: "medium", id: "ck.a", title: "A" })],
    });
    const out = renderFixPrompt(makeRenderInput({ file }));

    const idxPreamble = out.indexOf(PREAMBLE_PARAGRAPHS[0] ?? "");
    const idxSource = out.indexOf("Source report:");
    const idxRepo = out.indexOf("Frameworks:");
    const idxFindings = out.indexOf("## Findings");
    const idxNextSteps = out.indexOf("After addressing the findings");
    const idxFooter = out.indexOf("\n--\n");

    expect(idxPreamble).toBe(0);
    expect(idxSource).toBeGreaterThan(idxPreamble);
    expect(idxRepo).toBeGreaterThan(idxSource);
    expect(idxFindings).toBeGreaterThan(idxRepo);
    expect(idxNextSteps).toBeGreaterThan(idxFindings);
    expect(idxFooter).toBeGreaterThan(idxNextSteps);
  });

  it("sections separated by exactly one blank line; no `\\n\\n\\n` runs anywhere", () => {
    const file = makeReportFile({
      results: [makeResult({ level: "high", id: "ck.a", title: "A" })],
      task: "fix the bug",
      detectedFrameworks: ["pnpm", "vitest"],
      summary: "one finding",
    });
    const out = renderFixPrompt(makeRenderInput({ file }));
    expect(out.includes("\n\n\n")).toBe(false);
  });

  it("output ends with exactly one trailing LF (never zero, never two)", () => {
    const file = makeReportFile({
      results: [makeResult({ level: "high", id: "ck.a", title: "A" })],
    });
    const out = renderFixPrompt(makeRenderInput({ file }));
    expect(out.endsWith("\n")).toBe(true);
    expect(out.endsWith("\n\n")).toBe(false);
  });

  it("output contains LF line endings only — no `\\r` characters anywhere (CRLF / lone CR in dynamic content normalized away)", () => {
    // Even when dynamic content carries CRLF (Windows) or lone CR
    // (Mac classic) line endings, the renderer's inline + block
    // normalizers strip them to LF. The rendered prompt's "LF only"
    // promise (contract Rendered-output-format section) is global.
    const file = makeReportFile({
      results: [
        makeResult({
          level: "high",
          id: "ck.a",
          title: "Windows line ending\r\nshould be normalized",
          message: "Mac classic\rline ending\r\nmixed",
        }),
      ],
      task: "task\r\nwith CRLF",
      summary: "summary with\r\nCRLF inside",
    });
    const out = renderFixPrompt(makeRenderInput({ file }));
    expect(out.includes("\r")).toBe(false);
  });

  it("same input produces byte-identical output across repeated calls (determinism)", () => {
    // Two calls with the SAME input must return strictly-equal
    // strings. Catches a future template helper that sneaks in a
    // clock read / random read / ulid call (D29 + D90.5 prohibit
    // these, but a behavioral lock here adds a second safety net).
    const file = makeReportFile({
      results: [
        makeResult({ level: "high", id: "ck.b", title: "B" }),
        makeResult({ level: "critical", id: "ck.a", title: "A" }),
        makeResult({ level: "medium", id: "ck.c", title: "C" }),
      ],
      detectedFrameworks: ["pnpm", "vitest"],
      task: "deterministic fix",
      summary: "three findings",
    });
    const input = makeRenderInput({ file });
    const out1 = renderFixPrompt(input);
    const out2 = renderFixPrompt(input);
    expect(out1).toBe(out2);
  });
});

