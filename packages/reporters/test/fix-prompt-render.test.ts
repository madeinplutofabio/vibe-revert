// SECTION 2: task section conditional inclusion (D85.3)
// =============================================================================

describe("renderFixPrompt — task section (D85.3 conditional inclusion)", () => {
  it("renders `Task: <value>` between source attribution and repo context when file.report.task is non-empty", () => {
    const file = makeReportFile({
      results: [makeResult({ level: "high", id: "ck.a", title: "A" })],
      task: "fix the failing test",
    });
    const out = renderFixPrompt(makeRenderInput({ file }));
    const idxSource = out.indexOf("Source report:");
    const idxTask = out.indexOf("Task: fix the failing test");
    const idxRepo = out.indexOf("Frameworks:");
    expect(idxTask).toBeGreaterThan(idxSource);
    expect(idxRepo).toBeGreaterThan(idxTask);
  });

  it("omits the Task section entirely when file.report.task is undefined (no placeholder, no spurious blank gap)", () => {
    const file = makeReportFile({
      results: [makeResult({ level: "high", id: "ck.a", title: "A" })],
      // task NOT supplied → undefined on the optional field
    });
    const out = renderFixPrompt(makeRenderInput({ file }));
    expect(out.includes("Task:")).toBe(false);
    // Absent-section join MUST NOT create a triple-newline run.
    expect(out.includes("\n\n\n")).toBe(false);
  });

  it("normalizes a multi-line task to a single inline value before rendering", () => {
    // Inline normalization (D85.7): CRLF/lone-CR → LF → collapse
    // `\n` runs to space → trim. This test uses bare `\n` since
    // schema-level `nonBlankString` permits inner newlines.
    const file = makeReportFile({
      results: [makeResult({ level: "high", id: "ck.a", title: "A" })],
      task: "line one\nline two",
    });
    const out = renderFixPrompt(makeRenderInput({ file }));
    expect(out.includes("Task: line one line two")).toBe(true);
  });

  // Note: a whitespace-only task (e.g. "   ") would fail the schema's
  // `nonBlankString` constraint, so the "task normalizes to empty →
  // omitted" branch cannot be exercised through a schema-validated
  // fixture. That branch is hammered in fix-prompt-template.test.ts
  // (file 6) via direct `renderTaskSection` calls with hand-built
  // pre-schema inputs.
});

