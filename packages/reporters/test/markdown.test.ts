// =============================================================================
// SECTION 7: inline markdown escaping
// =============================================================================

describe("renderMarkdown — inline markdown escaping", () => {
  it("escapes backticks/asterisks/underscores/brackets in user-derived text; evidence content NOT escaped", () => {
    const output = renderMarkdown(
      makeRenderInput({
        file: makeReportFile({
          task: "Fix `auth` *flow* with [link]",
          results: [
            makeResult({
              level: "high",
              title: "Title with `backticks`",
              message: "Message with _underscores_",
              evidence: [{ detail: "Evidence with `backticks` not escaped" }],
            }),
          ],
          riskLevel: "high",
        }),
      }),
    );
    // Task: special chars escaped
    expect(output).toContain("\\`auth\\`");
    expect(output).toContain("\\*flow\\*");
    expect(output).toContain("\\[link\\]");
    // Title: backtick escaped
    expect(output).toContain("\\`backticks\\`");
    // Message: underscore escaped
    expect(output).toContain("\\_underscores\\_");
    // Evidence detail: NOT escaped (lives inside fenced block)
    expect(output).toContain("Evidence with `backticks` not escaped");
  });

  it("escapes < and > to HTML entities (no-HTML lock per D45)", () => {
    const output = renderMarkdown(
      makeRenderInput({
        file: makeReportFile({
          results: [
            makeResult({
              level: "high",
              title: "Title with <script>tag</script>",
            }),
          ],
          riskLevel: "high",
        }),
      }),
    );
    expect(output).toContain("&lt;script&gt;tag&lt;/script&gt;");
    expect(output).not.toContain("<script>");
  });
});

// =============================================================================
// SECTION 8: dynamic backtick fence
// =============================================================================

describe("renderMarkdown — dynamic backtick fence", () => {
  it("evidence containing triple backticks uses a 4-backtick fence to avoid breakout", () => {
    const output = renderMarkdown(
      makeRenderInput({
        file: makeReportFile({
          results: [
            makeResult({
              level: "high",
              evidence: [{ detail: "literal ``` triple backticks inside" }],
            }),
          ],
          riskLevel: "high",
        }),
      }),
    );
    // 4-backtick fence (longer than the 3 inside the evidence).
    expect(output).toContain("````");
    // Evidence content survives verbatim inside the longer fence.
    expect(output).toContain("literal ``` triple backticks inside");
  });
});
