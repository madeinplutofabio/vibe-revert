// SECTION 2: summary bullets
// =============================================================================

describe("renderReceiptMarkdown — summary bullets", () => {
  it("includes all 8 bullets with locked label text", () => {
    const file = makeReceiptFile({ mode: "apply" });
    const output = renderReceiptMarkdown(makeRenderInput({ file }));
    expect(output).toContain(`- **Rollback ID:** \`${ROLLBACK_ID}\``);
    expect(output).toContain(`- **Session ID:** \`${SESSION_ID}\``);
    expect(output).toContain(`- **Checkpoint ID:** \`${CHECKPOINT_ID}\``);
    expect(output).toContain("- **Mode:** apply");
    expect(output).toContain("- **Forced:** false");
    expect(output).toContain(`- **Pre-rollback CP:** \`${CHECKPOINT_ID}\``);
    expect(output).toContain("- **Dirty-tree check:** performed");
    expect(output).toContain("- **Written at:** 2026-01-01T00:00:00Z");
  });

  it("IDs are wrapped via inlineCode (single-backtick fence on backtick-free inputs)", () => {
    const file = makeReceiptFile({ mode: "apply" });
    const output = renderReceiptMarkdown(makeRenderInput({ file }));
    // Standard ULIDs contain no backticks → fence = 1 backtick.
    expect(output).toContain(`\`${ROLLBACK_ID}\``);
    expect(output).toContain(`\`${SESSION_ID}\``);
    expect(output).toContain(`\`${CHECKPOINT_ID}\``);
  });

  it("dry-run pre-rollback CP renders as '(none - dry-run)' placeholder (NO code span)", () => {
    const file = makeReceiptFile({ mode: "dry_run" });
    const output = renderReceiptMarkdown(makeRenderInput({ file }));
    expect(output).toContain("- **Pre-rollback CP:** (none - dry-run)");
    // The placeholder is plain text, not a code span — no surrounding backticks.
    expect(output).not.toContain("`(none - dry-run)`");
  });

  it("Mode and Dirty-tree check render via schemaToken (raw enum, no backslash escape)", () => {
    // The load-bearing assertion: snake_case values stay grep-able.
    // Per D69 bidirectional coupling, dirty_tree_check ===
    // "skipped_no_after_state" REQUIRES un_ended_session_warning ===
    // true. Including it here keeps the fixture schema-valid and
    // also semantically honest — "skipped after-state" should
    // always carry the warning.
    const file = makeReceiptFile({
      mode: "dry_run",
      dirtyTreeCheck: "skipped_no_after_state",
      unEndedSessionWarning: true,
    });
    const output = renderReceiptMarkdown(makeRenderInput({ file }));
    expect(output).toContain("- **Mode:** dry_run");
    expect(output).toContain("- **Dirty-tree check:** skipped_no_after_state");
    expect(output).not.toContain("dry\\_run");
    expect(output).not.toContain("skipped\\_no\\_after\\_state");
  });

  it("summary bullet order is locked (indexOf monotonicity check)", () => {
    const file = makeReceiptFile({ mode: "apply" });
    const output = renderReceiptMarkdown(makeRenderInput({ file }));
    const labels = [
      "- **Rollback ID:**",
      "- **Session ID:**",
      "- **Checkpoint ID:**",
      "- **Mode:**",
      "- **Forced:**",
      "- **Pre-rollback CP:**",
      "- **Dirty-tree check:**",
      "- **Written at:**",
    ];
    let lastIdx = -1;
    for (const label of labels) {
      const idx = output.indexOf(label);
      expect(idx).toBeGreaterThan(lastIdx);
      lastIdx = idx;
    }
  });
});

