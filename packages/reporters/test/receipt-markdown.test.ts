// Unit tests for packages/reporters/src/receipt-markdown.ts (M D
// Step 5 file 5.7c — receipt markdown-rendering coverage).
//
// Nine test sections covering the receipt-markdown contract:
//
//   1. BANNER — H1 with mode suffix ("(Dry-Run)" for dry-run mode).
//   2. SUMMARY BULLETS — 8 bullets in locked order; inlineCode for
//      IDs/paths (dynamic backtick fence per CommonMark §6.1);
//      schemaToken for enum values (snake_case preserved, no
//      backslash escape); pre-rollback CP placeholder for dry-run.
//   3. WARNINGS — blockquote per warning; blank-line separator
//      between consecutive warnings (REGRESSION GATE against the
//      CommonMark §5.1 blockquote-merge bug caught during 5.4 review).
//   4. FORCED UNRELATED DIRTY PATHS — section ENTIRELY ABSENT when
//      empty; H2 + bulleted inlineCode paths when populated.
//   5. RESULTS — H2; "No results." when empty; bullets with
//      bracketToken outcome + inlineCode path + optional sub-bullet
//      reason; consecutive bullets NOT separated by blank line
//      (CommonMark idiomatic tight list).
//   6. FAILURES — H2; "No failures." when empty; bullets with
//      bracketToken error_code + inlineMarkdown message + optional
//      "Affected paths:" sub-block.
//   7. OUT OF SCOPE — H2 always present; body is
//      inlineMarkdown(file.out_of_scope_notice) — equals
//      ROLLBACK_OUT_OF_SCOPE_NOTICE today since the literal is
//      pure ASCII.
//   8. FOOTER — "Generated by VibeRevert v<version>." paragraph;
//      version IS consumed (unlike terminal); two inputs differing
//      only by version produce DIFFERENT output.
//   9. LOCKED GUARANTEES + REGRESSION GATES — newline-terminated;
//      no GFM/HTML; the three load-bearing regression gates
//      (backtick-in-path dynamic fence, multi-backtick fence
//      escalation, backtick-prefix padding-branch coverage) +
//      token-not-escaped schemaToken protection lives in section 5/6.
//
// Fixture-validity discipline: every fixture flows through
// ReceiptFileSchema.parse via makeReceiptFile, so refine violations
// fail at fixture-construction time. Critical pairings the schema
// enforces (and our fixtures honor):
//   - active_session_warning: true → mode === "dry_run"
//   - dirty_tree_check === "skipped_no_after_state" ↔
//     un_ended_session_warning === true (bidirectional)
//   - forced_unrelated_dirty_paths populated → mode === "apply"
//     + forced === true + dirty_tree_check === "performed"

import {
  RECEIPT_FILE_SCHEMA_VERSION,
  type ReceiptFile,
  ReceiptFileSchema,
  ROLLBACK_OUT_OF_SCOPE_NOTICE,
} from "@viberevert/session-format";
import { describe, expect, it } from "vitest";

import { renderReceiptMarkdown } from "../src/receipt-markdown.js";
import type { ReceiptRenderInput } from "../src/receipt-types.js";

// =============================================================================
// Test fixtures (per-file duplication intentional, matching the
// existing reporter test convention)
// =============================================================================

const VALID_ULID = "01ABCDEFGHJKMNPQRSTVWXYZ23";
const ROLLBACK_ID = `rb_${VALID_ULID}`;
const SESSION_ID = `sess_${VALID_ULID}`;
const CHECKPOINT_ID = `cp_${VALID_ULID}`;

function makeReceiptFile(
  opts: {
    mode?: "dry_run" | "apply";
    forced?: boolean;
    preRollbackCheckpointId?: string | null;
    results?: ReceiptFile["results"];
    failures?: ReceiptFile["failures"];
    forcedUnrelatedDirtyPaths?: readonly string[];
    dirtyTreeCheck?: ReceiptFile["dirty_tree_check"];
    activeSessionWarning?: true;
    unEndedSessionWarning?: true;
  } = {},
): ReceiptFile {
  const mode = opts.mode ?? "apply";
  const preRollbackCp =
    opts.preRollbackCheckpointId !== undefined
      ? opts.preRollbackCheckpointId
      : mode === "apply"
        ? CHECKPOINT_ID
        : null;
  const base = {
    schema_version: RECEIPT_FILE_SCHEMA_VERSION,
    rollback_id: ROLLBACK_ID,
    session_id: SESSION_ID,
    checkpoint_id: CHECKPOINT_ID,
    mode,
    forced: opts.forced ?? false,
    written_at: "2026-01-01T00:00:00Z",
    pre_rollback_checkpoint_id: preRollbackCp,
    results: opts.results ?? [],
    failures: opts.failures ?? [],
    forced_unrelated_dirty_paths: opts.forcedUnrelatedDirtyPaths ?? [],
    dirty_tree_check: opts.dirtyTreeCheck ?? "performed",
    out_of_scope_notice: ROLLBACK_OUT_OF_SCOPE_NOTICE,
  };
  const receipt = {
    ...base,
    ...(opts.activeSessionWarning === true ? { active_session_warning: true as const } : {}),
    ...(opts.unEndedSessionWarning === true ? { un_ended_session_warning: true as const } : {}),
  };
  return ReceiptFileSchema.parse(receipt);
}

function makeRenderInput(opts: { file: ReceiptFile; productVersion?: string }): ReceiptRenderInput {
  return {
    file: opts.file,
    productVersion: opts.productVersion ?? "0.7.0-beta",
  };
}

// =============================================================================
// SECTION 1: banner
// =============================================================================

describe("renderReceiptMarkdown — banner", () => {
  it("apply mode: H1 is '# VibeRevert Rollback Receipt' (no suffix)", () => {
    const file = makeReceiptFile({ mode: "apply" });
    const output = renderReceiptMarkdown(makeRenderInput({ file }));
    expect(output.startsWith("# VibeRevert Rollback Receipt\n")).toBe(true);
    expect(output).not.toContain("Dry-Run");
  });

  it("dry-run mode: H1 is '# VibeRevert Rollback Receipt (Dry-Run)' (title-case suffix)", () => {
    const file = makeReceiptFile({ mode: "dry_run" });
    const output = renderReceiptMarkdown(makeRenderInput({ file }));
    expect(output.startsWith("# VibeRevert Rollback Receipt (Dry-Run)\n")).toBe(true);
    // Intentional divergence from terminal's "(DRY-RUN)" all-caps.
    expect(output).not.toContain("DRY-RUN");
  });
});

