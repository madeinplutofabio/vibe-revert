// SECTION 9: locked guarantees + regression gates
// =============================================================================

describe("renderReceiptMarkdown — locked guarantees", () => {
  it("output is newline-terminated", () => {
    const file = makeReceiptFile();
    const output = renderReceiptMarkdown(makeRenderInput({ file }));
    expect(output.endsWith("\n")).toBe(true);
  });

  it("REGRESSION GATE: path containing one backtick uses a 2-backtick fence", () => {
    // inlineCode picks fence length = longest backtick run + 1.
    // Path "src/weird`name.ts" has a single backtick → fence = 2.
    // Output: `- **[TRACKED_RESTORED]** ``src/weird`name.ts``` (2 fences around content with 1 backtick inside).
    // A regression to the unsafe `\`${inlineMarkdown(path)}\`` pattern
    // would either break the code span (escape doesn't work in
    // code spans per CommonMark §6.1) or render visible backslash
    // literals.
    const file = makeReceiptFile({
      results: [{ path: "src/weird`name.ts", outcome: "tracked_restored" }],
    });
    const output = renderReceiptMarkdown(makeRenderInput({ file }));
    expect(output).toContain("- **[TRACKED_RESTORED]** ``src/weird`name.ts``");
  });

  it("REGRESSION GATE: path containing a two-backtick run escalates fence to 3 backticks", () => {
    // Locks the dynamic-fence-length logic beyond the single-backtick
    // case. Path "src/double``name.ts" has 2 consecutive backticks
    // → fence = 3.
    const file = makeReceiptFile({
      results: [{ path: "src/double``name.ts", outcome: "tracked_restored" }],
    });
    const output = renderReceiptMarkdown(makeRenderInput({ file }));
    expect(output).toContain("- **[TRACKED_RESTORED]** ```src/double``name.ts```");
  });

  it("REGRESSION GATE: path beginning with a backtick uses padded inline-code content", () => {
    // Locks the `needsPadding` branch of inlineCode. Without
    // padding, content beginning with a backtick would merge with
    // the opening fence in CommonMark's parser — the 2-backtick
    // fence + leading backtick content would be read as a
    // 3-backtick delimiter, breaking the span. The space-padded
    // form `` `leading.ts `` parses correctly because the parser
    // strips one leading + one trailing space when both boundaries
    // have a space (per CommonMark §6.1).
    //
    // The other backtick regression tests use paths with backticks
    // in the MIDDLE — they don't exercise this padding branch and
    // would still pass under a regression that deleted the padding
    // logic entirely. This test is what catches that specific
    // regression class.
    const file = makeReceiptFile({
      results: [{ path: "`leading.ts", outcome: "tracked_restored" }],
    });
    const output = renderReceiptMarkdown(makeRenderInput({ file }));
    expect(output).toContain("- **[TRACKED_RESTORED]** `` `leading.ts ``");
  });

  it("NO raw HTML angle brackets in the output (D45 no-HTML lock)", () => {
    // inlineMarkdown HTML-encodes `<` and `>` so user-derived prose
    // can't form raw HTML. The schema-controlled fields (mode,
    // outcome, etc.) never contain angle brackets in practice, but
    // a reason or message with embedded `<script>` would be safely
    // escaped to `&lt;script&gt;`.
    const file = makeReceiptFile({
      results: [
        {
          path: "src/foo.ts",
          outcome: "tracked_restored",
          reason: "test <script>alert('xss')</script>",
        },
      ],
    });
    const output = renderReceiptMarkdown(makeRenderInput({ file }));
    expect(output).not.toContain("<script>");
    expect(output).toContain("&lt;script&gt;");
  });

  it("NO GFM table syntax (`| --- |` pipes-and-dashes) in the output (D45 lock)", () => {
    // The CommonMark-only lock forbids GFM table extensions. A
    // future "let's render results as a markdown table" temptation
    // would surface here.
    const file = makeReceiptFile({
      results: [{ path: "src/foo.ts", outcome: "tracked_restored" }],
      failures: [{ error_code: "verification", message: "test", affected_paths: ["src/bar.ts"] }],
    });
    const output = renderReceiptMarkdown(makeRenderInput({ file }));
    expect(output).not.toMatch(/\|\s*---+\s*\|/);
  });
});
