describe("renderReceiptMarkdown — footer", () => {
  it("emits 'Generated by VibeRevert v<version>.' as the final paragraph", () => {
    const file = makeReceiptFile();
    const output = renderReceiptMarkdown(makeRenderInput({ file, productVersion: "0.7.0-beta" }));
    expect(output).toContain("Generated by VibeRevert v0.7.0-beta.");
  });

  it("two inputs differing only by productVersion produce DIFFERENT output (footer consumes it)", () => {
    // Unlike terminal (which ignores productVersion entirely),
    // markdown bakes productVersion into the locked footer. Two
    // outputs MUST diverge — opposite invariant from the terminal
    // test's "byte-identical regardless of version" lock.
    const file = makeReceiptFile();
    const a = renderReceiptMarkdown(makeRenderInput({ file, productVersion: "0.7.0-beta" }));
    const b = renderReceiptMarkdown(makeRenderInput({ file, productVersion: "999.999.999-fake" }));
    expect(a).not.toBe(b);
    expect(a).toContain("0.7.0-beta");
    expect(b).toContain("999.999.999-fake");
  });

  it("productVersion is escaped via inlineMarkdown (defense against future version-string injection)", () => {
    // A productVersion containing markdown-active characters
    // (`*`, `_`, `` ` ``, brackets) must be escaped to prevent
    // emphasis/code/link formation in the footer paragraph. Today
    // productVersion comes from package.json and is trusted, but
    // the renderer escapes defensively per the same rule applied
    // to every other prose value.
    const file = makeReceiptFile();
    const output = renderReceiptMarkdown(
      makeRenderInput({ file, productVersion: "1.0.0-*alpha*" }),
    );
    // Asterisks should be backslash-escaped → "1.0.0-\*alpha\*"
    expect(output).toContain("Generated by VibeRevert v1.0.0-\\*alpha\\*.");
    expect(output).not.toContain("VibeRevert v1.0.0-*alpha*.");
  });
});

