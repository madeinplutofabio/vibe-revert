describe("renderReceiptMarkdown — failures section", () => {
  it("empty failures: H2 followed by 'No failures.' paragraph", () => {
    const file = makeReceiptFile();
    const output = renderReceiptMarkdown(makeRenderInput({ file }));
    expect(output).toContain("## Failures\n\nNo failures.");
  });

  it("single failure without affected_paths: '- **[ERROR_CODE]** message' bullet, no sub-block", () => {
    const file = makeReceiptFile({
      failures: [
        {
          error_code: "internal",
          message: "unexpected null in restore plan",
          affected_paths: [],
        },
      ],
    });
    const output = renderReceiptMarkdown(makeRenderInput({ file }));
    expect(output).toContain("- **[INTERNAL]** unexpected null in restore plan");
    expect(output).not.toContain("  - Affected paths:");
  });

  it("single failure with affected_paths: bullet + 'Affected paths:' sub-bullet + nested inlineCode bullets (4-space indent)", () => {
    const file = makeReceiptFile({
      failures: [
        {
          error_code: "extraction_conflict",
          message: "tarball entry would overwrite tracked file",
          affected_paths: ["src/conflict-a.ts", "src/conflict-b.ts"],
        },
      ],
    });
    const output = renderReceiptMarkdown(makeRenderInput({ file }));
    expect(output).toContain(
      "- **[EXTRACTION_CONFLICT]** tarball entry would overwrite tracked file",
    );
    expect(output).toContain("  - Affected paths:");
    expect(output).toContain("    - `src/conflict-a.ts`");
    expect(output).toContain("    - `src/conflict-b.ts`");
  });

  it("REGRESSION GATE: error_code token via schemaToken is RAW snake_case (NOT inlineMarkdown-escaped)", () => {
    const file = makeReceiptFile({
      failures: [
        {
          error_code: "tracked_dirty_parity",
          message: "tracked dirty paths do not match manifest",
          affected_paths: [],
        },
      ],
    });
    const output = renderReceiptMarkdown(makeRenderInput({ file }));
    expect(output).toContain("**[TRACKED_DIRTY_PARITY]**");
    expect(output).not.toContain("**[TRACKED\\_DIRTY\\_PARITY]**");
    expect(output).not.toContain("TRACKED\\_DIRTY\\_PARITY");
  });
});

