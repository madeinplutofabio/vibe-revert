describe("renderReceiptMarkdown — warnings", () => {
  it("both warnings absent: no '> **Warning:**' blockquote lines emitted", () => {
    const file = makeReceiptFile();
    const output = renderReceiptMarkdown(makeRenderInput({ file }));
    expect(output).not.toContain("> **Warning:**");
  });

  it("only active_session_warning set: only the active-session blockquote is emitted", () => {
    const file = makeReceiptFile({ mode: "dry_run", activeSessionWarning: true });
    const output = renderReceiptMarkdown(makeRenderInput({ file }));
    expect(output).toContain(
      "> **Warning:** Active session - apply mode would refuse on this session.",
    );
    expect(output).not.toContain("> **Warning:** No machine-readable after-status snapshot");
  });

  it("only un_ended_session_warning set: only the un-ended blockquote is emitted", () => {
    const file = makeReceiptFile({
      mode: "dry_run",
      dirtyTreeCheck: "skipped_no_after_state",
      unEndedSessionWarning: true,
    });
    const output = renderReceiptMarkdown(makeRenderInput({ file }));
    expect(output).toContain(
      "> **Warning:** No machine-readable after-status snapshot for this session.",
    );
    expect(output).not.toContain("> **Warning:** Active session");
  });

  it("REGRESSION GATE: dual warnings render as two distinct blockquote blocks (blank line between)", () => {
    // Per CommonMark §5.1, consecutive `>` lines without an
    // intervening blank line form ONE blockquote block. The
    // implementation inserts a blank line between warnings so they
    // render as DISTINCT blocks. A regression that dropped the
    // blank-line separator would merge both warnings into one
    // wrapped paragraph in most renderers — locked behavior worth
    // pinning explicitly so a future "simplify the buildWarnings
    // logic" refactor can't silently weaken it.
    const file = makeReceiptFile({
      mode: "dry_run",
      dirtyTreeCheck: "skipped_no_after_state",
      activeSessionWarning: true,
      unEndedSessionWarning: true,
    });
    const output = renderReceiptMarkdown(makeRenderInput({ file }));
    expect(output).toContain(
      "> **Warning:** Active session - apply mode would refuse on this session.\n\n> **Warning:** No machine-readable after-status snapshot for this session.",
    );
  });
});

