describe("renderReceiptMarkdown — forced unrelated dirty paths section", () => {
  it("empty: section is ENTIRELY ABSENT (no H2, no list)", () => {
    const file = makeReceiptFile();
    const output = renderReceiptMarkdown(makeRenderInput({ file }));
    expect(output).not.toContain("## Forced Unrelated Dirty Paths");
  });

  it("populated: H2 'Forced Unrelated Dirty Paths' + bulleted inlineCode paths", () => {
    // forced_unrelated_dirty_paths is only schema-valid when
    // mode: "apply" + forced: true + dirty_tree_check: "performed".
    const file = makeReceiptFile({
      mode: "apply",
      forced: true,
      dirtyTreeCheck: "performed",
      forcedUnrelatedDirtyPaths: ["src/dirty-a.ts", "src/dirty-b.ts"],
    });
    const output = renderReceiptMarkdown(makeRenderInput({ file }));
    expect(output).toContain("## Forced Unrelated Dirty Paths");
    expect(output).toContain("- `src/dirty-a.ts`");
    expect(output).toContain("- `src/dirty-b.ts`");
  });

  it("single path: still rendered as a single-item list", () => {
    const file = makeReceiptFile({
      mode: "apply",
      forced: true,
      dirtyTreeCheck: "performed",
      forcedUnrelatedDirtyPaths: ["src/only.ts"],
    });
    const output = renderReceiptMarkdown(makeRenderInput({ file }));
    expect(output).toContain("## Forced Unrelated Dirty Paths");
    expect(output).toContain("- `src/only.ts`");
  });
});

