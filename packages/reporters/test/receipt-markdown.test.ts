describe("renderReceiptMarkdown — results section", () => {
  it("empty results: H2 followed by 'No results.' paragraph", () => {
    const file = makeReceiptFile();
    const output = renderReceiptMarkdown(makeRenderInput({ file }));
    expect(output).toContain("## Results\n\nNo results.");
  });

  it("single result without reason: '- **[OUTCOME]** `path`' bullet, no sub-bullet", () => {
    const file = makeReceiptFile({
      results: [{ path: "src/foo.ts", outcome: "tracked_restored" }],
    });
    const output = renderReceiptMarkdown(makeRenderInput({ file }));
    expect(output).toContain("- **[TRACKED_RESTORED]** `src/foo.ts`");
    expect(output).not.toContain("  - Reason:");
  });

  it("single result with reason: bullet + sub-bullet 'Reason: ...' (2-space indent)", () => {
    const file = makeReceiptFile({
      results: [
        {
          path: "src/foo.ts",
          outcome: "skipped_excluded",
          reason: "matches rollback.exclude pattern test.ts",
        },
      ],
    });
    const output = renderReceiptMarkdown(makeRenderInput({ file }));
    expect(output).toContain("- **[SKIPPED_EXCLUDED]** `src/foo.ts`");
    expect(output).toContain("  - Reason: matches rollback.exclude pattern test.ts");
  });

  it("multiple results: bullets are consecutive WITHOUT blank line between (CommonMark tight list)", () => {
    // Unlike terminal which inserts blank-line separators, markdown
    // packs consecutive bullets into a tight list per CommonMark
    // convention. A regression that inserted blank lines would
    // produce a loose list with extra paragraph spacing.
    const file = makeReceiptFile({
      results: [
        { path: "src/a.ts", outcome: "tracked_restored" },
        { path: "src/b.ts", outcome: "untracked_restored" },
      ],
    });
    const output = renderReceiptMarkdown(makeRenderInput({ file }));
    expect(output).toContain(
      "- **[TRACKED_RESTORED]** `src/a.ts`\n- **[UNTRACKED_RESTORED]** `src/b.ts`",
    );
  });

  it("REGRESSION GATE: outcome token via schemaToken is RAW snake_case (NOT inlineMarkdown-escaped)", () => {
    // A regression that reverts schemaToken → inlineMarkdown would
    // escape every underscore as `\_`, producing
    // **[TRACKED\_RESTORED]** in the rendered source — polluting
    // grep-ability and contradicting the receipt-markdown.ts layout
    // lock. This test catches that regression at the source-bytes
    // level via both targeted and broad substring checks.
    const file = makeReceiptFile({
      results: [{ path: "src/foo.ts", outcome: "tracked_restored" }],
    });
    const output = renderReceiptMarkdown(makeRenderInput({ file }));
    expect(output).toContain("**[TRACKED_RESTORED]**");
    expect(output).not.toContain("**[TRACKED\\_RESTORED]**");
    expect(output).not.toContain("TRACKED\\_RESTORED");
  });
});

